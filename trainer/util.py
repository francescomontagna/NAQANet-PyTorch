def normalize_answer(s):
    """Convert to lowercase and remove punctuation, articles and extra whitespace."""

    def remove_articles(text):
        regex = re.compile(r'\b(a|an|the)\b', re.UNICODE)
        return re.sub(regex, ' ', text)

    def white_space_fix(text):
        return ' '.join(text.split())

    def remove_punc(text):
        exclude = set(string.punctuation)
        return ''.join(ch for ch in text if ch not in exclude)

    def lower(text):
        return text.lower()

    return white_space_fix(remove_articles(remove_punc(lower(s))))


def get_tokens(s):
    if not s:
        return []
    return normalize_answer(s).split()


def compute_em(a_gold, a_pred):
    return int(normalize_answer(a_gold) == normalize_answer(a_pred))


def compute_f1(a_gold, a_pred):
    gold_toks = get_tokens(a_gold)
    pred_toks = get_tokens(a_pred)
    common = Counter(gold_toks) & Counter(pred_toks)
    num_same = sum(common.values())
    if len(gold_toks) == 0 or len(pred_toks) == 0:
        # If either is no-answer, then F1 is 1 if they agree, 0 otherwise
        return int(gold_toks == pred_toks)
    if num_same == 0:
        return 0
    precision = 1.0 * num_same / len(pred_toks)
    recall = 1.0 * num_same / len(gold_toks)
    f1 = (2 * precision * recall) / (precision + recall)
    return f1

def build_spans(context_tokens_list:list, q_ids: list, start_indices:list, end_indices:list):
    """

    :param context_tokens_list: list with the tokenized context sentences. [ ['word1', 'word2', ...], [sentence #2], ... [sentence #batch_size]]
    :param q_ids: list of strings, elements are ID of the questions. len(q_ids) = batch size
    :param start_indices: list of the start indices of the answers. len(start_indices) = batch size. Must be paired with end_indices elements
    :param end_indices: list of the end indices of the answers. len(end_indices) = batch size.
    :return: dictionary with (K = question id, V = string answer)
    """
    batch_size = len(q_ids)
    answer_spans = dict()
    for i in range(batch_size):
        passage = context_tokens_list[i]
        qid = q_ids[i]
        pred = ''
        for index in range(start_indices[i], end_indices[i]):
            word = passage[index]
            pred += word + ' '
        answer_spans[qid] = pred[:-1] # answer will be normalized in evaluate method

    return answer_spans

def tokenize(sequence):
    tokens = [token.replace("``", '"').replace("''", '"') for token in word_tokenize(sequence)]
    return tokens



def make_eval_dict(dev_data_filepath, output_path):
    """

    :param dev_data_filepath: evaluation dataset json path
    :param output_path: where to store the processed dataset. It is a dictionary where (K='question_id', V=[answers])
    """
    # Posso prendere le risposte, tokenizzarle e ricomporle per avere coerenza con quelle date dalla rete

    eval_json = load_json(dev_data_filepath)
    data = eval_json['data']
    eval_dict = dict()

    for paragraphs in data:

        for para in paragraphs['paragraphs']:
            for qa in para['qas']:
                id = qa['id']
                answers = qa['answers']
                eval_dict[id] = answers

    with open(output_path, 'w') as f:
        json.dump(eval_dict, f)


def make_eval_dict_tokens(dev_data_filepath, output_path):
    """

    :param dev_data_filepath: evaluation dataset json path
    :param output_path: where to store the processed dataset. It is a dictionary where (K='question_id', V=[answers])
    """
    # Posso prendere le risposte, tokenizzarle e ricomporle per avere coerenza con quelle date dalla rete

    eval_json = load_json(dev_data_filepath)
    data = eval_json['data']
    eval_dict = dict()

    for paragraphs in data:

        for para in paragraphs['paragraphs']:
            for qa in para['qas']:
                id = qa['id']
                answers = qa['answers']
                from_tokens_answers = []
                for answer in answers:
                    tokens = tokenize(answer['text'])
                    ground_truth = ''
                    for t in tokens:
                        ground_truth += t + ' '
                    from_tokens_answers.append(ground_truth[:-1])

                eval_dict[id] = from_tokens_answers

    with open(output_path, 'w') as f:
        json.dump(eval_dict, f)

